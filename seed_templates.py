async def seed():
    async with AsyncSessionLocal() as db:
        # Check if already seeded
        count_q = select(func.count()).select_from(
            select(MissionTemplate)
            .where(MissionTemplate.is_builtin.is_(True))
            .subquery()
        )
        existing = (await db.execute(count_q)).scalar() or 0
        if existing > 0:
            print(f"⚠️  {existing} built-in templates already exist — skipping seed")
            return

        for tpl in TEMPLATES:
            record = MissionTemplate(
                id=uuid.uuid4(),
                user_id=1,
                name=tpl["name"],
                description=tpl["description"],
                category=tpl["category"],
                icon=tpl["icon"],
                is_public=True,
                is_builtin=True,
                mission_type=None,
                priority=tpl["priority"],
                default_plan=tpl["default_plan"],
                default_tasks=None,
                default_constraints=None,
                tags=None,
                usage_count=0,
            )
            db.add(record)
            print(f"  ✓ {tpl['name']}")

        await db.commit()
        print(f"\n✅ Seeded {len(TEMPLATES)} built-in templates")


if __name__ == "__main__":
    asyncio.run(seed())
